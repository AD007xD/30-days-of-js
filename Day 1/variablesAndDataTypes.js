//!! Data Types

//! There are 5 primitive data types in JS
//* String
//* Number
//* Boolean
//* Undefined
//* Null

console.log('String:', 'This is a string');
console.log('Number:', 10);
console.log('Boolean:', true);
console.log('Undefined:', undefined);
console.log('Null:', null);

//` String is a set of characters
//` Number is, well, a number
//` Boolean is a condition, i.e. either 'true' or 'false'
//` Undefined is when a variable is not defined
//` Null is when a variable is set to null
// note: null == undefined is true, but null === undefined is false

//!! Variables
//! There are 3 ways to declare variables
//* Const (Constant)
//* Let
//* Var (Deprecated)

const PI = 3.14;
let date = '17-1-2023';
var mood = null;

//!! Checking the data type of a variable
//* you can use the 'typeof' operator to find the data type of the variable
console.log(typeof PI);
console.log(typeof date);
console.log(typeof mood);
